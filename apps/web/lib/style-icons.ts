export function iconToDataUri(name: string, color = "#ffffff"): string | null {
  const body = ICON_BODIES[name]
  if (!body) return null
  return `data:image/svg+xml;utf8,${encodeURIComponent(buildSvg(body, color))}`
}

export function iconSvgString(
  name: string,
  color = "currentColor"
): string | null {
  const body = ICON_BODIES[name]
  if (!body) return null
  return buildSvg(body, color)
}

